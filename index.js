function logMessage(message) {
  console.log(message);
}
logMessage(123);
logMessage('Message from code');


function logForUser(userName, word) {
  console.log(`${word}, ${userName}!`);
}

logForUser('Hi', 'Bill'); 
logForUser('Tom', 'Ciao'); 
logForUser('Anna', 'Halo'); 

